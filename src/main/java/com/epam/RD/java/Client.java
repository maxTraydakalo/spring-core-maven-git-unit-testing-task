package com.epam.RD.java;

public class Client {
    Long id;
    String fullName;

    Client(){}

    Client(long id, String fullName){
        this.id = id;
        this.fullName = fullName;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getFullName() {
        return fullName;
    }

    public void setFullName(String fullName) {
        this.fullName = fullName;
    }
}
